package ca.nrc.cadc.caom2;

import ca.nrc.cadc.search.form.HierarchyUType;
import ca.nrc.cadc.date.DateUtil;
import org.springframework.jdbc.core.RowMapper;

import java.sql.ResultSet;
import java.sql.ResultSetMetaData;
import java.sql.SQLException;
import java.sql.Timestamp;
import java.util.ArrayList;
import java.util.Calendar;
import java.util.Date;
import java.util.List;

public class GroupRowMapper implements RowMapper
{


    /**
     * Implementations must implement this method to map each row of data
     * in the ResultSet. This method should not call <code>next()</code> on
     * the ResultSet; it is only supposed to map values of the current row.
     *
     * @param rs     the ResultSet to map (pre-initialized for the current row)
     * @param rowNum the number of the current row
     * @return the result object for the current row
     * @throws SQLException if a SQLException is encountered getting
     *                      column values (that is, there's no need to catch SQLException)
     */
    @Override
    public Object mapRow(final ResultSet rs, final int rowNum)
            throws SQLException
    {
        final ResultSetMetaData resultSetMetaData = rs.getMetaData();
        final int columnCount = resultSetMetaData.getColumnCount();
        final List<String> combo = new ArrayList<>(columnCount);

        for (int i = 1; i <= columnCount; i++)
        {
            final String columnName = resultSetMetaData.getColumnName(i);

            // Special case to remap.
            if (columnName.equalsIgnoreCase(
                    HierarchyUType.MAX_LAST_MODIFIED_COLUMN_NAME))
            {
                final Timestamp timestamp = rs.getTimestamp(i);
                combo.add(Boolean.toString(new Date(timestamp.getTime()).after(
                        getComparisonDate())));
            }
            else
            {
                combo.add(rs.getString(i));
            }
        }

        return combo;
    }

    /**
     * Current rule says any instrument with a date from now until now - five
     * years is fresh.
     *
     * @return      Date of now minus five years.
     */
    Date getComparisonDate()
    {
        final Calendar cal = Calendar.getInstance(DateUtil.UTC);
        cal.add(Calendar.YEAR, -5);

        return cal.getTime();
    }
}
