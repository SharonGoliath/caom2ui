package ca.nrc.cadc.search.validate;

public abstract class NumberValidator
{
    protected String utype;
    protected Number value;
    protected String unit;

    protected NumberValidator(final String utype, final Number value,
                              final String unit)
    {
        this.utype = utype;
        this.value = value;
        this.unit = unit;
    }

    protected NumberValidator()
    {
        // empty
    }

    public void setUtype(final String utype)
    {
        this.utype = utype;
    }

    public void setValue(final Number value)
    {
        this.value = value;
    }

    public void setUnit(final String unit)
    {
        this.unit = unit;
    }

    public abstract Double validate() throws ValidationException;

}
